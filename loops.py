# simple loop, to count a bunch of numbers
for i in [0, 1, 2, 3, 4, 5]: # python list
    print(i)

# ---

for i in range(6):  # instead of writing [0, 1, 2, 3, 4, 5], use a built-in function range()
    print(i)

